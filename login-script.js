// LANGUAGE MANAGEMENT
// ==========================================
function changeLanguage(lang) {
    // Save language preference
    localStorage.setItem('preferred-lang', lang);

    // Update page title
    const pageTitleKey = document.querySelector('[data-i18n="page-title"]');
    if (pageTitleKey) {
        document.title = loginTranslations[lang]['page-title'];
    }

    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (loginTranslations[lang] && loginTranslations[lang][key]) {
            element.textContent = loginTranslations[lang][key];
        }
    });

    // Update all elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (loginTranslations[lang] && loginTranslations[lang][key]) {
            element.placeholder = loginTranslations[lang][key];
        }
    });

    // Add smooth transition
    document.body.style.opacity = '1';
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferred-lang') || 'en';
    document.getElementById('language-select').value = savedLang;
    changeLanguage(savedLang);
});

